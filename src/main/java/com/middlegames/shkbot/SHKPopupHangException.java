package com.middlegames.shkbot;

@SuppressWarnings("serial")
public class SHKPopupHangException extends SHKException {

	public SHKPopupHangException() {
		super();
	}

	public SHKPopupHangException(String message, Throwable cause) {
		super(message, cause);
	}

	public SHKPopupHangException(String message) {
		super(message);
	}

	public SHKPopupHangException(Throwable cause) {
		super(cause);
	}
}
